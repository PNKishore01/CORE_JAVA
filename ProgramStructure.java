class ProgramStructure{

    //Block of statements

    public static void main(String[] args){

        //Block of statements

        System.out.println("Hi, Welcome to the Java");
    }
}

/**
 * Class -> Predefined Keyword (Reserved Keyword for java)
 * ProgramStructure - UserDefined DataType (It is also called java className)
 * public -> Access Modifier (it can access upto JVM)
 * static -> Non-Access Modifier 
 * void -> Non-return type
 * main -> methodName
 * (String[] args) -> parameters
 * System -> Predefined Class
 * out -> variable or object
 * println -> predefined method
 * ("Hi, Welcome to the Java") -> user friendly message
 * ";" -> semi-colon should mandatory for every end of statemnet
 */